<h2 id="说明" tabindex="-1"><a class="header-anchor" href="#说明" aria-hidden="true">#</a> 说明 💎</h2>
<h3 id="后端开发为什么选择nodejs或者python而不选择java" tabindex="-1"><a class="header-anchor" href="#后端开发为什么选择nodejs或者python而不选择java" aria-hidden="true">#</a> 后端开发为什么选择NodeJs或者Python而不选择Java? 👻</h3>
<p>首先，许多语言都能够完成后端开发的工作，如Php、Python、Java、NodeJS、Go等等，可以肯定的是，Java占据着国内后端开发老大哥的地位，是主流的后端开发语言，但是由于近几年的行业形势，Java入门门槛越来越高，开发人员也远大于岗位数量，没有足够的工作经验很难找到满意的工作，更别说刚入门的Java程序员了。Php曾是世界上最好的编程语言(有这么一个梗)，目前已很少有人在使用了。而Go语言，都是一些大厂在使用，如果你感觉自己可以去大厂，可以考虑一下Go语言。</p>
<p>选择Nodejs进行后端开发，能够快速的搭建好后台，为我们的前端开发提供所需的接口，并且生态丰富，大多数功能的实现都能找到相关插件、无需自己编写，直接使用即可，其主要语言依旧是JavaScript，只是依靠了node环境而已，对于前端工程师来说，使用NodeJs开发后端门槛和成本是十分低的，因此NodeJs是个不错的选择。</p>
<p>Python最近几年逐步兴起，有这么一句话，python除了生孩子，其他都能做，可见Python也是被大家肯定的开发语言了，可能你听说过Python要比Java慢，确实Python在一开始设计的时候进没有考虑内核处理器问题，这是Python本身存在的缺陷，随着Python3版本的出现，Python越来越被大家所认可，在游戏开发、人工智能、数据分析等方面更是起着举足轻重的作用，我个人之所以用Python做后端开发语言的原因是，Python语法简介，入门门槛低，在我看来，Python一点都不慢，想象一下，自己在以后的开发中，开发出的网站会不会有好几十万好几百万甚至更多的人同时在线，会不会出现像双十一那种全国大半人口戳手机屏幕抢购的情况，我个人认为在以后的工作中开发的网站应用同时在线上千人上万人已经算是难遇的了，即使超过了数十万数百万，Python也已经足够了。</p>
